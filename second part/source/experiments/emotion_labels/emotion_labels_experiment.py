def discretize_level(value_norm: float) -> Optional[str]:
    if pd.isna(value_norm):
        return None
    if value_norm < LOW_THRESH:
        return 'low'
    elif value_norm < HIGH_THRESH:
        return 'medium'
    else:
        return 'high'


def map_to_emotion(arousal_norm: float, valence_norm: float) -> Optional[str]:
    a_level = discretize_level(arousal_norm)
    v_level = discretize_level(valence_norm)
    if a_level is None or v_level is None:
        return None
    return EMOTION_MAP.get((a_level, v_level), 'neutral')


def extract_emotion_intervals(df, arousal_scale, valence_scale):
    """Extract emotion label intervals from arousal/valence time series."""
    df = df.copy().sort_values('seconds').reset_index(drop=True)
    intervals = []
    current_emotion = None
    start_time = None

    for idx, row in df.iterrows():
        a = row.get('arousal', np.nan)
        v = row.get('valence', np.nan)

        a_norm = normalize_value(a, arousal_scale[0], arousal_scale[1])
        v_norm = normalize_value(v, valence_scale[0], valence_scale[1])
        emotion = map_to_emotion(a_norm, v_norm)
        time = row['seconds']

        if emotion is None:
            if current_emotion is not None and start_time is not None:
                end_time = time
                if start_time < end_time:
                    intervals.append({'state': f'emo_{current_emotion}',
                                      'start_time': start_time, 'end_time': end_time})
            current_emotion = None
            start_time = None
            continue

        if emotion != current_emotion:
            if current_emotion is not None and start_time is not None:
                end_time = time
                if start_time < end_time:
                    intervals.append({'state': f'emo_{current_emotion}',
                                      'start_time': start_time, 'end_time': end_time})
            current_emotion = emotion
            start_time = time

    if current_emotion is not None and start_time is not None:
        last_time = df['seconds'].iloc[-1] + WINDOW_SIZE
        if start_time < last_time:
            intervals.append({'state': f'emo_{current_emotion}',
                              'start_time': start_time, 'end_time': last_time})

    return intervals


def process_participant_discrete(df, participant_id, dataset_name,
                                  global_thresholds, arousal_scale, valence_scale):
    """Convert participant data to ARMADA format with emotion labels + physio."""
    if df is None or len(df) == 0:
        return None

    all_intervals = []

    # Emotion labels (from arousal x valence)
    emotion_intervals = extract_emotion_intervals(df, arousal_scale, valence_scale)
    all_intervals.extend(emotion_intervals)

    # Physio states (same as standard pipeline)
    for var in PHYSIO_VARIABLES:
        if var not in df.columns:
            continue
        if var not in global_thresholds:
            continue
        gt = global_thresholds[var]

        if var in PERSONAL_NORM_VARIABLES:
            col_to_use = f'{var}_norm'
            if col_to_use not in df.columns:
                df = df.copy()
                df[col_to_use] = normalize_personal_minmax(df, var)
            intervals = extract_state_intervals(df, col_to_use, gt)
            readable_name = VARIABLE_NAME_MAPPING.get(var, var)
            for iv in intervals:
                iv['state'] = iv['state'].replace(col_to_use, readable_name)
            all_intervals.extend(intervals)
        else:
            intervals = extract_state_intervals(df, var, gt)
            readable_name = VARIABLE_NAME_MAPPING.get(var, var)
            for iv in intervals:
                iv['state'] = iv['state'].replace(var, readable_name)
            all_intervals.extend(intervals)

    if not all_intervals:
        return None

    result_df = pd.DataFrame(all_intervals)
    result_df['client_id'] = f"{dataset_name}_{participant_id}"
    result_df = result_df[result_df['start_time'] < result_df['end_time']]
    result_df = result_df.drop_duplicates(subset=['state', 'start_time', 'end_time'])
    result_df = result_df[['client_id', 'state', 'start_time', 'end_time']]
    result_df = result_df.sort_values(['start_time', 'end_time', 'state']).reset_index(drop=True)
    return result_df


def build_discrete_armada(dataset_name, processed_dir, arousal_scale, valence_scale,
                           global_thresholds):
    """Build discrete emotion ARMADA data for one dataset."""
    csv_files = sorted(processed_dir.glob("*_merged.csv"))
    if not csv_files:
        print(f"  No files found in {processed_dir}")
        return None

    results = []
    for csv_file in csv_files:
        participant_id = csv_file.stem.replace("_merged", "")
        try:
            df = pd.read_csv(csv_file)
            result = process_participant_discrete(
                df, participant_id, dataset_name,
                global_thresholds, arousal_scale, valence_scale
            )
            if result is not None and len(result) > 0:
                results.append(result)
        except Exception as e:
            print(f"  {participant_id}: Error - {e}")

    if not results:
        return None

    combined = pd.concat(results, ignore_index=True)
    print(f"  {dataset_name}: {len(csv_files)} participants, {len(combined)} intervals")
    return combined


def get_rule_details(rules):
    details = {}
    for r in rules:
        sig = f"{r.antecedent.get_relation_description()} => {r.consequent.get_relation_description()}"
        details[sig] = {
            'confidence': r.confidence,
            'support': r.support,
            'count': r.consequent.support_count
        }
    return details


def main():
    OUTPUT_DIR = SCRIPT_DIR / "results"
    OUTPUT_DIR.mkdir(parents=True, exist_ok=True)

    print("=" * 80)
    print("EXPERIMENT: DIMENSIONAL vs DISCRETE EMOTIONAL MODEL")
    print("=" * 80)
    print(f"Date: {pd.Timestamp.now().strftime('%Y-%m-%d %H:%M')}")
    print()

    # Compute cross-dataset thresholds from all processed dirs
    threshold_dirs = {}
    for name, proc_dir, _, _, _ in DATASETS:
        if proc_dir.exists():
            threshold_dirs[name] = proc_dir
    global_thresholds = compute_cross_dataset_thresholds(threshold_dirs)

    # ===== DIMENSIONAL: use existing armada_ready files =====
    print("\n" + "=" * 80)
    print("DIMENSIONAL MODEL (existing armada_ready files)")
    print("=" * 80)

    dim_results = {}
    for name, path in DIMENSIONAL_FILES.items():
        if not path.exists():
            print(f"  {name}: MISSING {path.name}")
            continue
        print(f"\n  Processing {name}...")
        armada, patterns, rules = run_armada_on_df(
            pd.read_csv(path), MINSUP, MINCONF, MAXGAP, MAX_PATTERN_SIZE
        )
        raw_sigs = extract_rule_signatures(rules)
        filtered = filter_rules(raw_sigs, FILTER_BVP_ONLY, FILTER_EDA_ONLY,
                                FILTER_PHYSIO_CROSS, FILTER_SINGLE_FEATURE)
        dim_results[name] = {
            'n_participants': armada.num_clients,
            'n_raw': len(rules),
            'n_filtered': len(filtered),
            'filtered': filtered,
        }
        print(f"    N={armada.num_clients}, raw={len(rules)}, filtered={len(filtered)}")

    # ===== DISCRETE: build emotion-label data and run ARMADA =====
    print("\n" + "=" * 80)
    print("DISCRETE MODEL (emotion labels from Russell's circumplex)")
    print("=" * 80)

    disc_results = {}
    for name, proc_dir, a_scale, v_scale, ann_type in DATASETS:
        if not proc_dir.exists():
            print(f"  {name}: MISSING directory")
            continue

        print(f"\n  Building discrete data for {name}...")
        armada_df = build_discrete_armada(name, proc_dir, a_scale, v_scale, global_thresholds)
        if armada_df is None:
            continue

        # Save and run ARMADA
        tmp_path = OUTPUT_DIR / f"armada_discrete_{name}.csv"
        save_armada_format(armada_df, tmp_path)

        armada, patterns, rules = run_armada_on_df(
            pd.read_csv(tmp_path), MINSUP, MINCONF, MAXGAP, MAX_PATTERN_SIZE
        )
        raw_sigs = extract_rule_signatures(rules)
        filtered = filter_rules(raw_sigs, FILTER_BVP_ONLY, FILTER_EDA_ONLY,
                                FILTER_PHYSIO_CROSS, FILTER_SINGLE_FEATURE)

        # Count emotion-involving rules
        emo_rules = {s for s in filtered if 'emo_' in s}

        disc_results[name] = {
            'n_participants': armada.num_clients,
            'n_raw': len(rules),
            'n_filtered': len(filtered),
            'n_emotion': len(emo_rules),
            'filtered': filtered,
            'emo_rules': emo_rules,
        }
        print(f"    N={armada.num_clients}, raw={len(rules)}, filtered={len(filtered)}, "
              f"emotion-involving={len(emo_rules)}")

    # ===== COMPARISON =====
    print(f"\n{'='*80}")
    print("COMPARISON: DIMENSIONAL vs DISCRETE")
    print(f"{'='*80}")

    # Union across all datasets
    dim_union = set()
    for r in dim_results.values():
        dim_union |= r['filtered']

    disc_union = set()
    for r in disc_results.values():
        disc_union |= r['filtered']

    # Intersection across all datasets (rules present in EVERY dataset)
    dim_sets = [r['filtered'] for r in dim_results.values() if r['filtered']]
    disc_sets = [r['filtered'] for r in disc_results.values() if r['filtered']]

    dim_intersect = dim_sets[0].copy() if dim_sets else set()
    for s in dim_sets[1:]:
        dim_intersect &= s

    disc_intersect = disc_sets[0].copy() if disc_sets else set()
    for s in disc_sets[1:]:
        disc_intersect &= s

    # Intersection per-pair (pairwise shared rules)
    dataset_names = [name for name, _, _, _, _ in DATASETS]

    print(f"\n  Dimensional union:     {len(dim_union)} unique rules")
    print(f"  Discrete union:        {len(disc_union)} unique rules")
    print(f"  Dimensional intersect: {len(dim_intersect)} rules in ALL datasets")
    print(f"  Discrete intersect:    {len(disc_intersect)} rules in ALL datasets")

    if dim_intersect:
        print(f"\n  Dimensional rules shared by ALL datasets:")
        for r in sorted(dim_intersect):
            print(f"    {r}")

    if disc_intersect:
        print(f"\n  Discrete rules shared by ALL datasets:")
        for r in sorted(disc_intersect):
            print(f"    {r}")

    # Pairwise Jaccard similarity
    print(f"\n  Pairwise Jaccard — Dimensional:")
    for i, n1 in enumerate(dataset_names):
        if n1 not in dim_results:
            continue
        for n2 in dataset_names[i+1:]:
            if n2 not in dim_results:
                continue
            j = jaccard_similarity(dim_results[n1]['filtered'], dim_results[n2]['filtered'])
            shared_count = len(dim_results[n1]['filtered'] & dim_results[n2]['filtered'])
            print(f"    {n1} x {n2}: Jaccard={j:.3f}, shared={shared_count}")

    print(f"\n  Pairwise Jaccard — Discrete:")
    for i, n1 in enumerate(dataset_names):
        if n1 not in disc_results:
            continue
        for n2 in dataset_names[i+1:]:
            if n2 not in disc_results:
                continue
            j = jaccard_similarity(disc_results[n1]['filtered'], disc_results[n2]['filtered'])
            shared_count = len(disc_results[n1]['filtered'] & disc_results[n2]['filtered'])
            print(f"    {n1} x {n2}: Jaccard={j:.3f}, shared={shared_count}")

    # ===== GENERATE REPORT =====
    lines = []
    lines.append("# Dimensional vs Discrete Emotional Model — Comparison")
    lines.append("")
    lines.append("## Parameters")
    lines.append("")
    lines.append(f"| Parameter | Value |")
    lines.append(f"|-----------|-------|")
    lines.append(f"| minsup | {MINSUP} |")
    lines.append(f"| minconf | {MINCONF} |")
    lines.append(f"| maxgap | {MAXGAP}s |")
    lines.append(f"| max_pattern_size | {MAX_PATTERN_SIZE} |")
    lines.append("")

    lines.append("## Per-Dataset Results")
    lines.append("")
    lines.append("| Dataset | Ann. | Dim Filt | Disc Filt | Ratio |")
    lines.append("|---------|------|----------|-----------|-------|")
    for name, _, _, _, ann_type in DATASETS:
        d = dim_results.get(name, {})
        s = disc_results.get(name, {})
        d_filt = d.get('n_filtered', 0)
        s_filt = s.get('n_filtered', 0)
        ratio = f"{s_filt/d_filt:.2f}" if d_filt > 0 else "—"
        lines.append(f"| {name} | {ann_type} | {d_filt} | {s_filt} | {ratio} |")
    lines.append("")

    lines.append("## Cross-Dataset Comparison")
    lines.append("")
    lines.append(f"| Metric | Dimensional | Discrete |")
    lines.append(f"|--------|-------------|----------|")
    lines.append(f"| Union (unique rules) | {len(dim_union)} | {len(disc_union)} |")
    lines.append(f"| Intersection (in ALL datasets) | {len(dim_intersect)} | {len(disc_intersect)} |")
    lines.append("")

    if dim_intersect:
        lines.append("### Dimensional Rules Shared by ALL Datasets")
        lines.append("")
        for r in sorted(dim_intersect):
            lines.append(f"- `{r}`")
        lines.append("")

    if disc_intersect:
        lines.append("### Discrete Rules Shared by ALL Datasets")
        lines.append("")
        for r in sorted(disc_intersect):
            lines.append(f"- `{r}`")
        lines.append("")

    # Pairwise Jaccard table
    lines.append("## Pairwise Jaccard Similarity — Dimensional")
    lines.append("")
    header = "| | " + " | ".join(dataset_names) + " |"
    sep = "|---|" + "|".join(["---"] * len(dataset_names)) + "|"
    lines.append(header)
    lines.append(sep)
    for n1 in dataset_names:
        row = f"| **{n1}** |"
        for n2 in dataset_names:
            if n1 == n2:
                row += " 1.000 |"
            elif n1 in dim_results and n2 in dim_results:
                j = jaccard_similarity(dim_results[n1]['filtered'], dim_results[n2]['filtered'])
                row += f" {j:.3f} |"
            else:
                row += " — |"
        lines.append(row)
    lines.append("")

    lines.append("## Pairwise Jaccard Similarity — Discrete")
    lines.append("")
    lines.append(header)
    lines.append(sep)
    for n1 in dataset_names:
        row = f"| **{n1}** |"
        for n2 in dataset_names:
            if n1 == n2:
                row += " 1.000 |"
            elif n1 in disc_results and n2 in disc_results:
                j = jaccard_similarity(disc_results[n1]['filtered'], disc_results[n2]['filtered'])
                row += f" {j:.3f} |"
            else:
                row += " — |"
        lines.append(row)
    lines.append("")

    report_path = OUTPUT_DIR / "emotion_labels_report.md"
    with open(report_path, "w") as f:
        f.write("\n".join(lines))
    print(f"\nReport saved: {report_path}")

    print(f"\n{'='*80}")
    print("DONE")
    print(f"{'='*80}")


if __name__ == "__main__":
    main()
