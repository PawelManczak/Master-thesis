#!/usr/bin/env python3
"""
Leave-One-Out Universal Rules - Combined Variant

This script replicates the Leave-One-Out validation architecture, but calculates
training rules by pooling N-1 datasets into a single aggregated DataFrame. The globally
robust training rules are then tested on the separate Nth Test dataset.
"""

import sys
from pathlib import Path
import pandas as pd
import numpy as np

SCRIPT_DIR = Path(__file__).parent
EXPERIMENTS_DIR = SCRIPT_DIR.parent
PROJECT_DIR = EXPERIMENTS_DIR.parent.parent

sys.path.insert(0, str(PROJECT_DIR / "source" / "processing" / "armada"))
sys.path.insert(0, str(PROJECT_DIR / "source" / "processing" / "extracting"))
sys.path.insert(0, str(EXPERIMENTS_DIR))

from experiment_utils import (
    run_armada_on_df,
    extract_rule_signatures,
    filter_rules,
)

MINSUP = 0.1
MINCONF = 0.5
MAXGAP = 5
MAX_PATTERN_SIZE = 2

FILTER_BVP_ONLY = True
FILTER_EDA_ONLY = True
FILTER_PHYSIO_CROSS = True
FILTER_SINGLE_FEATURE = True

DATA_DIR = PROJECT_DIR / "data" / "armada_ready"

DATASETS = {
    "CASE":       DATA_DIR / "armada_case.csv",
    "K-emoCon":   DATA_DIR / "armada_k_emocon.csv",
    "CEAP":       DATA_DIR / "armada_ceap.csv",
    "EmoWorker":  DATA_DIR / "armada_emoworker_v2.csv",
}

OUTPUT_DIR = SCRIPT_DIR / "results"
OUTPUT_DIR.mkdir(parents=True, exist_ok=True)


def get_filtered_rules(df, dataset_name_prefix=None):
    """Run ARMADA and get a dict of {signature: rule_object}."""
    if dataset_name_prefix:
        df = df.copy()
        df['client_id'] = f"{dataset_name_prefix}_" + df['client_id'].astype(str)
        
    armada, patterns, rules = run_armada_on_df(df, MINSUP, MINCONF, MAXGAP, MAX_PATTERN_SIZE)
    
    raw_sigs = extract_rule_signatures(rules)
    filtered_sigs = filter_rules(
        raw_sigs,
        FILTER_BVP_ONLY,
        FILTER_EDA_ONLY,
        FILTER_PHYSIO_CROSS,
        FILTER_SINGLE_FEATURE
    )

    sig_to_rule = {}
    for r in rules:
        sig = f"{r.antecedent.get_relation_description()} => {r.consequent.get_relation_description()}"
        if sig in filtered_sigs:
            sig_to_rule[sig] = r
            
    return sig_to_rule


def generate_markdown_report(report_data, output_dir):
    lines = [
        "# Leave-One-Out Aggregated Universal Rules",
        "",
        "This version computes rules using N-1 merged datasets (global support threshold)",
        "and checks for their survival exclusively on the held-out test set.",
        "",
        "## Parameters",
        f"| Parameter | Value |",
        "|-----------|-------|",
        f"| minsup (Train Pool) | {MINSUP} |",
        f"| minsup (Test Set) | {MINSUP} |",
        f"| minconf | {MINCONF} |",
        "",
        "## Results Summary",
        ""
    ]

    # Convert results dict into dataframe for clean table
    rows = []
    for test_ds, data in report_data.items():
        rows.append({
            "Test Set": test_ds,
            "Rules generated by Train Pool": len(data['train_sigs']),
            "Rules discovered in Test Set": len(data['test_sigs']),
            "Rules crossing over (Generalization)": len(data['intersection']),
            "Missing in Test Set": len(data['train_sigs']) - len(data['intersection'])
        })
        
    df = pd.DataFrame(rows)
    lines.append("| Test Set | Train Pool Rules | Test Set Rules | Generalized Rules | Failed Rules |")
    lines.append("|----------|------------------|----------------|-------------------|--------------|")
    for _, row in df.iterrows():
        lines.append(f"| **{row['Test Set']}** | {row['Rules generated by Train Pool']} "
                     f"| {row['Rules discovered in Test Set']} | **{row['Rules crossing over (Generalization)']}** "
                     f"| {row['Missing in Test Set']} |")

    lines.append("")
    lines.append("## Validated Generalized Rules (by Test Set)")
    lines.append("")
    
    for test_ds, data in report_data.items():
        lines.append(f"### Trained on others, tested on {test_ds}")
        if data['intersection']:
            lines.append("| Rule | Train Confidence | Test Confidence |")
            lines.append("|------|------------------|-----------------|")
            
            # Sort by Train Confidence 
            survivors = []
            for sig in data['intersection']:
                t_conf = data['train_rules'][sig].confidence
                s_conf = data['test_rules'][sig].confidence
                survivors.append((sig, t_conf, s_conf))
                
            survivors.sort(key=lambda x: x[1], reverse=True)
            for sig, tc, sc in survivors:
                cleansig = sig.replace('|', '\\|')
                lines.append(f"| `{cleansig}` | {tc:.4f} | {sc:.4f} |")
        else:
            lines.append("*No rules from the training pool generalized to this test set successfully.*")
        lines.append("")

    report_path = output_dir / "universal_rules_leave_one_out_combined_report.md"
    report_path.write_text("\n".join(lines), encoding="utf-8")
    return report_path


def main():
    print("=" * 80)
    print("LEAVE ONE OUT: COMBINED AGGREGATION")
    print(f"Parameters: minsup={MINSUP}, minconf={MINCONF}")
    print("=" * 80)

    self_datasets = {
        'CASE': DATA_DIR / "armada_case.csv",
        'K-emoCon': DATA_DIR / "armada_k_emocon.csv",
        'CEAP': DATA_DIR / "armada_ceap.csv",
        'EmoWorker_v2': DATA_DIR / "armada_emoworker_v2.csv"
    }

    external_datasets = {
        'K-emo_ext': DATA_DIR / "armada_k_emocon_ext.csv",
        'EMBOA': DATA_DIR / "armada_emboa.csv"
    }

    combined_datasets = {**self_datasets, **external_datasets}

    experiment_scenarios = {
        'self': self_datasets,
        'external': external_datasets,
        'external_self': combined_datasets
    }

    BASE_OUTPUT_DIR = OUTPUT_DIR

    for scenario_name, datasets in experiment_scenarios.items():
        print(f"\n" + "#"*80)
        print(f"RUNNING SCENARIO: {scenario_name.upper()}")
        print(f"#"*80)

        CURRENT_OUTPUT_DIR = BASE_OUTPUT_DIR / scenario_name
        CURRENT_OUTPUT_DIR.mkdir(parents=True, exist_ok=True)

        loaded_dfs = {}
        for ds_name, data_file in datasets.items():
            if not data_file.exists():
                print(f"WARNING: {data_file} not found, skipping...")
                continue
            loaded_dfs[ds_name] = pd.read_csv(data_file)

        if len(loaded_dfs) < 2:
            print(f"Not enough valid datasets for {scenario_name}. Skipping.")
            continue

        report_data = {}

        for test_ds_name in sorted(loaded_dfs.keys()):
            print(f"\n{'-'*60}")
            print(f"Testing generalization on TEST SET: {test_ds_name}")

            train_dfs = []
            for name, df in loaded_dfs.items():
                if name != test_ds_name:
                    df_copy = df.copy()
                    df_copy['client_id'] = f"{name}_" + df_copy['client_id'].astype(str)
                    train_dfs.append(df_copy)

            # 1. Train on Combined Pool
            print(f"  Training global rules on merged ({len(train_dfs)} sets)...")
            df_train_pool = pd.concat(train_dfs, ignore_index=True)
            train_rules_map = get_filtered_rules(df_train_pool)

            # 2. Extract rules from isolated Test Set
            print(f"  Extracting rules from isolated Test Set...")
            test_rules_map = get_filtered_rules(loaded_dfs[test_ds_name], dataset_name_prefix=test_ds_name)

            # 3. Intersect signatures
            train_sigs = set(train_rules_map.keys())
            test_sigs = set(test_rules_map.keys())
            intersection = train_sigs & test_sigs

            report_data[test_ds_name] = {
                'train_sigs': train_sigs,
                'test_sigs': test_sigs,
                'train_rules': train_rules_map,
                'test_rules': test_rules_map,
                'intersection': intersection
            }

            print(f"  -> Generated {len(train_sigs)} core theory rules.")
            print(f"  -> {len(intersection)} rules survived validation in test test.")

        print(f"\n{'-'*60}")
        out_path = generate_markdown_report(report_data, CURRENT_OUTPUT_DIR)
        print(f"Finished. Results saved to: {out_path}")

    print("\nALL SCENARIOS COMPLETED.")


if __name__ == "__main__":
    main()
